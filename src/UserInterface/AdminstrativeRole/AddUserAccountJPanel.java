    private void cmbOrganizationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbOrganizationActionPerformed
        // TODO add your handling code here:
       Organization o = (Organization) cmbOrganization.getSelectedItem();
        if(o!=null){
            populateCombo(o);
            populateRole(o);
        }
    }//GEN-LAST:event_cmbOrganizationActionPerformed

    private void populateCombo(Organization organization){
        if(cmbRole.getSelectedItem() == null){
            return;
        }
        personCmb.removeAllItems();
        for (Employee person : organization.getEmployeeDirectory().getEmployeeList()) {
            personCmb.addItem(person);
        }

    }
    
    private void populateRole(Organization organization){
        if(cmbOrganization.getSelectedItem() == null){
            return;
        }
        cmbRole.removeAllItems();
        for(Role role : organization.getSupportedRole()){
            cmbRole.addItem(role);
        }
    }
    
    private void populateOrganization(){
        cmbOrganization.removeAllItems();            
        for(Organization organization : business.getOrganizationDirectory().getOrganizationDirectory()){
            cmbOrganization.addItem(organization);
        }
    }
