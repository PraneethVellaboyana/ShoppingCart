    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        // TODO add your handling code here:
        String uName = usernameTxt.getText().trim();
        String password = passwordTxt.getText().trim();
        
        if(uName.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter a value");
            return;
        }

        if(uName.equalsIgnoreCase("Admin")){
            JOptionPane.showMessageDialog(null, "Admin already exists. Admin can't be duplicate");
            return;
        }
         Employee person = (Employee)personCmb.getSelectedItem();
        if(person==null){
            JOptionPane.showMessageDialog(null, "Please create an Employee.", "Warning Message", JOptionPane.WARNING_MESSAGE);
            return;
        }
        for(Organization org: business.getOrganizationDirectory().getOrganizationDirectory()){
            for(UserAccount ua: org.getUserAccountDirectory().getUserAccountDirectory()){
                if(ua.getUserName().equals(uName)){
                    JOptionPane.showMessageDialog(null, "UserAccount already exists. Please try another Name.");
                    return;
                }
            }
        }
        UserAccount userAccount=null;
        Role role = (Role)cmbRole.getSelectedItem();
        Organization o = (Organization) cmbOrganization.getSelectedItem();
        if(o!=null){
             userAccount = o.getUserAccountDirectory().addAccount();
             userAccount.setUserName(uName);
             userAccount.setPassword(password);
        
        if(role.toString().equals(Role.RoleType.Customer.getValue())){
        Customer customer = (Customer) personCmb.getSelectedItem();
        userAccount.setEmployee(customer);
        }
        if( role.toString().equals(Role.RoleType.Supplier.getValue())){
            Supplier supplier = (Supplier) personCmb.getSelectedItem();
            userAccount.setEmployee(supplier);
        } else {
        }
        userAccount.setRole(role);
        if(activeRadio.isSelected())
               userAccount.setStatus("Active");
        else
               userAccount.setStatus("Disabled");
        JOptionPane.showMessageDialog(null, "UserAccount has been created for: "+userAccount.getUserName());
        usernameTxt.setText("");
        passwordTxt.setText("");
        }
    }//GEN-LAST:event_submitBtnActionPerformed

