    private void populateTable(){
        
        DefaultTableModel dtm = (DefaultTableModel)tblReqStatus.getModel();
        dtm.setRowCount(0);
        
        for(WorkRequest wr: userAccount.getWorkQueue().getWorkRequests()){
            Object[] row = new Object[5];
            
            row[0] = wr.getReceiver()==null?"To be Assigned":wr.getReceiver();
            row[1] = wr.getStatus();
            row[2] = wr.getRequestDate();
            row[3] = wr.getResolveDate()==null?"To be Resolved":wr.getResolveDate();
            EnrollmentWorkRequest ewr = (EnrollmentWorkRequest)wr;
            row[4] = ewr.getEnrollmentStatus()==null?"Response Awaited":ewr.getEnrollmentStatus();
            
            dtm.addRow(row);
        }
    }
