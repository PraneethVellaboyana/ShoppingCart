    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)tblReqStatus.getModel();
        
        int count = dtm.getRowCount();
        
        if(count>=1){
            JOptionPane.showMessageDialog(null,"Request has already been submitted.");
            return;
        }
        String reqMessage = txtReqMsg.getText().trim();
        
        if(reqMessage.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter a message for approval.");
            return;
        }
        
        EnrollmentWorkRequest enrollReq = new EnrollmentWorkRequest();
        enrollReq.setSender(userAccount);
        enrollReq.setStatus(WorkRequest.Status.SENT.getValue());
        enrollReq.setMessage(reqMessage);
        enrollReq.getRequestDate();
        
        Organization org = null;
        
        for(Organization o: business.getOrganizationDirectory().getOrganizationDirectory()){
            if(o instanceof SalesSpecialistOrganization){
                org=o;
                break;
            }
        }
        
        userAccount.getWorkQueue().getWorkRequests().add(enrollReq);
        if(org!=null)
        org.getWorkQueue().getWorkRequests().add(enrollReq);
        
        populateTable();
    }//GEN-LAST:event_btnRequestActionPerformed


